#include "library.h"

int	ft_isascii(char *line)
{
	int	i;

	i = 0;
	while (line[i])
	{
		if ((line[i] >= 0 && line[i] < 127) || line[i] == 127)
			i++;
		else
			return (0);
	}
	return (1);
}
